#!/bin/bash

# 위 shebang은 bash로 되어 있지만, zsh에서도 호환됩니다.

echo "---------------------------------------------------------"
echo "| #  | Path                                              |"
echo "---------------------------------------------------------"

# PATH 내용을 줄 바꿈으로 바꾸어 루프에서 각 줄을 읽습니다.
# IFS는 기본값인 공백과 탭 등을 사용하며, read -r은 역슬래시 이스케이프를 방지합니다.
idx=1
echo "$PATH" | tr ':' '\n' | while read -r path; do
    # 경로가 빈 문자열이 아닌 경우에만 출력합니다. (가끔 PATH에 빈 항목이 있을 수 있음)
    if [ -n "$path" ]; then
        printf "| %-2s | %-45s |\n" "$idx" "$path"
        idx=$((idx + 1))
    fi
done

echo "---------------------------------------------------------"